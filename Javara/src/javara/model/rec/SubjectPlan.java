package javara.model.rec;

public class SubjectPlan {
	
	private int sp_No;
	private String sp_Account;
	private String sp_Acc_Detail;
	private String sp_Acc_Item;
	private Double sp_Amount;
	private String purposeofUse;
	private int si_No;
	
	public int getSp_No() {
		return sp_No;
	}
	public void setSp_No(int sp_No) {
		this.sp_No = sp_No;
	}
	public String getSp_Account() {
		return sp_Account;
	}
	public void setSp_Account(String sp_Account) {
		this.sp_Account = sp_Account;
	}
	public String getSp_Acc_Detail() {
		return sp_Acc_Detail;
	}
	public void setSp_Acc_Detail(String sp_Acc_Detail) {
		this.sp_Acc_Detail = sp_Acc_Detail;
	}
	public String getSp_Acc_Item() {
		return sp_Acc_Item;
	}
	public void setSp_Acc_Item(String sp_Acc_Item) {
		this.sp_Acc_Item = sp_Acc_Item;
	}
	public Double getSp_Amount() {
		return sp_Amount;
	}
	public void setSp_Amount(Double sp_Amount) {
		this.sp_Amount = sp_Amount;
	}
	public String getPurposeofUse() {
		return purposeofUse;
	}
	public void setPurposeofUse(String purposeofUse) {
		this.purposeofUse = purposeofUse;
	}
	public int getSi_No() {
		return si_No;
	}
	public void setSi_No(int si_No) {
		this.si_No = si_No;
	}

}
